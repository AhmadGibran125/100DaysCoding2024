
package Coding100day;

import java.util.Scanner;

public class Day51 {
    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        
        String huruf = in.nextLine();
        int angka = Integer.parseInt(huruf);
        switch (angka){
            case 01 :
                System.out.println("Tanggal lahir saya");
                break ;
            case 31 :
                System.out.println("Bulan lahir saya");
                break ;
            case 2006 :
                System.out.println("Tahun lahir saya");
                break ;
            default :
                System.out.println("mana gue tau");
                
                
            
               
        
    }
    }
}
