
package Coding100day;

public class Day57 {
    public static void main(String[] args) {
        int angka = 1;
        
        // Looping menggunakan while 
        while (angka < 10){
           System.out.println("Niali Angka : " + angka);
            angka++; //increment  
        }
        System.out.println("Looping Sudah Selesai Nilai akhir : "+ angka);
    }
            
}
