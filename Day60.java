package Coding100day;

public class Day60 {
    public static void main(String[] args) {
        
        int angka = 0;
        
        do{
            System.out.print(angka+" ");
            angka++;    
        }while (angka < 10);
        
        int angka1 = 10;
        do{
            System.out.print(angka1+" ");
            angka1--;
        }while (angka1 >= 0);
        
    }
}
